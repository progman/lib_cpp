// get file size
off64_t libcore::get_file_size(int handle)
{
	struct stat my_stat;
	int rc = ::fstat(handle, &my_stat);
	if (rc == -1)
	{
		return -1;
	}

	return my_stat.st_size;
}
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
// get file size
off64_t libcore::get_file_size(const char *pfilename)
{
	struct stat my_stat;
	int rc = ::stat(pfilename, &my_stat);
	if (rc == -1)
	{
		return -1;
	}

	return my_stat.st_size;
}
