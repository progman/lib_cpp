// get env var and convert to bool
bool libcore::env2bool(bool &result, bool value_default, const char *pname)
{
	char *p = getenv(pname);
	if (p == NULL)
	{
		result = value_default;
		return false;
	}

	if (libcore::str2bool(result, value_default, p) == false)
	{
		return false;
	}

	return true;
}
