uint64_t libcore::rnd(uint64_t min, uint64_t max)
{
	if (min == max)
	{
		return min;
	}

	if (max < min)
	{
		uint64_t tmp = min;
		min = max;
		max = tmp;
	}

	uint64_t rnd = uint64_t((rand()/((double)RAND_MAX)) * (max - min + 1)) + min;

	return (rnd > max) ? max : rnd;
}
