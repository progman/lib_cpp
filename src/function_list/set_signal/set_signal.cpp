// set signal
bool libcore::set_signal(int signo, void (*sig_handler)(int))
{
	struct sigaction act, oldact;
	act.sa_handler = sig_handler;
	sigemptyset(&act.sa_mask);
	act.sa_flags = 0;
	if (signo != SIGALRM)
	{
		act.sa_flags |= SA_RESTART;
	}

	if (sigaction(signo, &act, &oldact) == -1) return false;

	return true;
}
